/**
 * Copy static files to dist
 */
function copyStaticFiles() {
  const staticFiles = ['.nojekyll', 'robots.txt'];
  
  staticFiles.forEach(file => {
    const srcPath = file;
    const destPath = path.join(OUTPUT_DIR, file);
    
    if (fs.existsSync(srcPath)) {
      fs.copyFileSync(srcPath, destPath);
      console.log(`✓ Copied: ${file} -> ${destPath}`);
    }
  });
}

/**
 * Main execution
 */
function main() {
  console.log('Starting MCP Registry endpoint generation...\n');
  
  // Read the registry
  const registry = readRegistry();
  console.log(`Found ${registry.servers?.length || 0} servers in registry\n`);
  
  // Clean and create output directory
  if (fs.existsSync(OUTPUT_DIR)) {
    fs.rmSync(OUTPUT_DIR, { recursive: true });
  }
  ensureDir(OUTPUT_DIR);
  
  // Generate endpoints
  generateServersEndpoint(registry);
  generateServerDetailEndpoints(registry);
  generateIndexPage(registry);
  copyStaticFiles();
  
  console.log('\n✅ Registry endpoint generation completed successfully!');
  console.log(`\nOutput directory: ${OUTPUT_DIR}`);
}

// Run the script
main();
